<?php
///////  列印星期，方法1:餘數判斷  ///////////////////
for($a=1;$a<=31;$a++)
{
  echo $a."　";
  if(($a%7)==0)
  {
    echo "<br>";
  }
}
echo "<p>";

//////  列印星期，方法2:再多借用一個變數來計算列印數量  //////////////////
$nu=0;
for($a=1;$a<=31;$a++)
{
  echo $a."　";
  $nu++;
  if($nu==7)
  {
    echo "<br>";
    $nu=0;
  }
}
?>